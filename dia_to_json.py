def generate_dialogue_json(file: str, r: list):
    os.makedirs(os.path.dirname(file), exist_ok=True)
    # 读取已有文件
    if os.path.exists(file):
        with open(file, "r", encoding="utf-8") as f:
            try:
                existing_lines = json.load(f)
                if not isinstance(existing_lines, list):
                    existing_lines = []
            except json.JSONDecodeError:
                existing_lines = []
    else:
        existing_lines = []
    # 合并去重
    combined_lines = existing_lines + [line for line in r if line not in existing_lines]
    # 写回 JSON 文件
    with open(file, "w", encoding="utf-8") as f:
        json.dump(combined_lines, f, ensure_ascii=False, indent=4)
    print(f"生成/更新文件: {file}，新增台词数量: {len(combined_lines) - len(existing_lines)}")


def main():
    dd = "./dialogue/"
    for k, v in roles.items():
        generate_dialogue_json(dd + k + ".json", v)


main()
